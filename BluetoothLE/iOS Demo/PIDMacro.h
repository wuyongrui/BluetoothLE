#ifndef PIDMacro_h
#define PIDMacro_h

#define SCREEN_WIDTH         [UIScreen mainScreen].bounds.size.width
#define SCREEN_HEIGHT        [UIScreen mainScreen].bounds.size.height
#define PIDRealHeight(_h) (_h * SCREEN_HEIGHT / 667)
#define PIDRealWidth(_w) (_w * SCREEN_WIDTH / 375)

#endif /* PIDMacro_h */
